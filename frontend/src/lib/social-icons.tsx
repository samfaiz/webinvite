/** Human-readable label for a platform slug. */
export function socialLabel(key: string): string {
  const map: Record<string, string> = {
    x: "X",
    ig: "Instagram",
    in: "LinkedIn",
    fb: "Facebook",
    yt: "YouTube",
    wa: "WhatsApp",
    gh: "GitHub",
  };
  const p = (key || "").toLowerCase().trim();
  if (map[p]) return map[p];
  return p ? p.charAt(0).toUpperCase() + p.slice(1) : "Link";
}
